#include <stdio.h>

int main() {
    // Байты в точности как в вашем формате
    unsigned char bytes[] = {
    // N = 10 (0x0A)
    0x0A, 0x00, 0x00, 0x00,
    
    // Элементы (key, value)
    0x1F, 0x00, 0x00, 0x00, 0x1E, 0x00, 0x00, 0x00,  // key=0x1F, value=0x1E
    0x0E, 0x00, 0x00, 0x00, 0x24, 0x00, 0x00, 0x00,  // key=0x0E, value=0x24
    0x05, 0x00, 0x00, 0x00, 0x1B, 0x00, 0x00, 0x00,  // key=0x05, value=0x1B
    0x10, 0x00, 0x00, 0x00, 0x06, 0x00, 0x00, 0x00,  // key=0x10, value=0x06
    0x22, 0x00, 0x00, 0x00, 0x11, 0x00, 0x00, 0x00,  // key=0x22, value=0x11
    0x0A, 0x00, 0x00, 0x00, 0x11, 0x00, 0x00, 0x00,  // key=0x0A, value=0x11
    0x25, 0x00, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00,  // key=0x25, value=0x05
    0x0E, 0x00, 0x00, 0x00, 0x1D, 0x00, 0x00, 0x00,  // key=0x0E, value=0x1D
    0x25, 0x00, 0x00, 0x00, 0x1D, 0x00, 0x00, 0x00,  // key=0x25, value=0x1D
    0x0E, 0x00, 0x00, 0x00, 0x1B, 0x00, 0x00, 0x00   // key=0x0E, value=0x1B
};
    
    int bytes_count = sizeof(bytes);
    
    FILE* file = fopen("input.txt", "wb");
    if (file == NULL) {
        printf("Ошибка открытия файла!\n");
        return 1;
    }
    
    size_t written = fwrite(bytes, 1, bytes_count, file);
    
    if (written != bytes_count) {
        printf("Ошибка записи в файл!\n");
        fclose(file);
        return 1;
    }
    
    fclose(file);
    printf("Успешно записано %d байт в файл 'output.bin'\n", bytes_count);
    
    return 0;
}